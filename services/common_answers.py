def _score_match(problem_key: str, keywords: tuple[str, ...]) -> int:
    score = 0
    words = set(problem_key.split())
    for keyword in keywords:
        key = normalize_text_key(keyword)
        if key in problem_key:
            score += 3 + len(key.split())
            continue
        key_words = set(key.split())
        overlap = words & key_words
        if key_words and len(overlap) == len(key_words):
            score += 2
        elif overlap:
            score += 1
    return score

def get_common_initial_advice(problem: str) -> str:
    text = normalize_text_key(problem)
    best_score = 0
    best_answer = ""
    for item in COMMON_INITIAL_ANSWERS:
        score = _score_match(text, item["keywords"])
        if score > best_score:
            best_score = score
            best_answer = item["answer"]
    return best_answer if best_score >= 3 else ""
